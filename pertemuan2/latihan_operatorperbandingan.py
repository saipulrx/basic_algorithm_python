# Ambil input untuk mengisi nilai
print("Latihan Operator Perbandingan")
print("===========================")
a = input("Inputkan nilai a: ")
b = input("Inputkan nilai b: ")

nilai_a = int(a)
nilai_b = int(b)

# Menggunakan operator perbandingan >
print("")
print("Contoh Operator perbandingan >")
c = nilai_a > nilai_b
print ("Hasil a > b = ", c)

# Operator perbandingan <
print("")
print("Contoh Operator perbandingan <")
c = nilai_a < nilai_b
print ("Hasil a < b = ", c)

# Operator perbandingan ==
print("")
print("Contoh Operator perbandingan == ")
c = nilai_a == nilai_b
print ("Hasil a == b = ",c)

# Operator perbandingan !=
print("")
print("Contoh Operator perbandingan != ")
c = nilai_a != nilai_b
print ("Hasil a != b = ",c)

# Operator perbandingan >=
print("")
print("Contoh Operator perbandingan >= ")
c = nilai_a >= nilai_b
print ("Hasil a >= b = ",c)

# Operator perbandingan <=
print("")
print("Contoh Operator perbandingan <= ")
c = nilai_a <= nilai_b
print ("Hasil a <= b = ",c)

