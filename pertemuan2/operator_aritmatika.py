# Ambil input untuk mengisi nilai
print("Latihan Operator Aritmatika")
print("===========================")
a = input("Inputkan nilai a: ")
b = input("Inputkan nilai b: ")

nilai_a = int(a)
nilai_b = int(b)

# Menggunakan operator penjumlahan
print("Contoh Operator Aritmatika Operasi Penjumlahan")
c = nilai_a + nilai_b
print ("Hasil a + b = ", c)

# Operator Pengurangan
print("Contoh Operator Aritmatika Operasi Pengurangan")
c = nilai_a - nilai_b
print ("Hasil a - b = ", c)

# Operator Perkalian
print("Contoh Operator Aritmatika Operasi Perkalian")
c = nilai_a * nilai_b
print ("Hasil a * b = ",c)

# Operator Pembagian
print("Contoh Operator Aritmatika Operasi Pembagian")
c = nilai_a / nilai_b
print ("Hasil a / b = ",c)

# Operator Sisa Bagi
print("Contoh Operator Aritmatika Operasi Sisa Bagi")
c = nilai_a % nilai_b
print ("Hasil a %% b = ",c)

# Operator Pangkat
print("Contoh Operator Aritmatika Operasi Pangkat")
c = nilai_a ** nilai_b
print ("Hasil a ** b = ",c)

print("===========================")